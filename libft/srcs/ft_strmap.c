#include "libft.h"

char		*ft_strmap(char const *s, char (*f)(char))
{
	char	*fresh;
	int		i;
	size_t	len;

	if (!s || !f)
		return (0);
	len = ft_strlen(s) + 1;
	if (!(fresh = (char *)malloc(sizeof(char) * len)))
		return (NULL);
	i = 0;
	while (s[i])
	{
		fresh[i] = f(s[i]);
		i++;
	}
	fresh[i] = '\0';
	return (fresh);
}
