#include "libft.h"

char		*ft_strrchr(const char *str, int c)
{
	char	*ptr;

	ptr = NULL;
	while (*str)
	{
		if (*str == c)
			ptr = (char *)str;
		str++;
	}
	if (!c)
		ptr = (char *)str++;
	return (ptr);
}
