#include "libft.h"

void		ft_putnbr(int i)
{
	if (i == MININT)
		write(1, "-2147483648", 11);
	else
	{
		if (i < 0)
		{
			write(1, "-", 1);
			i *= -1;
		}
		if (i > 9)
		{
			ft_putnbr(i / 10);
			ft_putnbr(i % 10);
		}
		else
		{
			i += 48;
			write(1, &i, 1);
		}
	}
}
